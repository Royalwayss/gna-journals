<h1 class="page-title">Peer Review &amp; Publication Policy</h1>
      <h2>Desk Rejections</h2>
      <p>The submitted manuscript to GJMT initially undergoes a preliminary review at the Editorial Desk to assess its suitability. Manuscripts that do not align with the journal's focus or lack quality in terms of content and methodology may be rejected at this stage. Based on the editors' recommendation, manuscripts deemed suitable are forwarded to expert reviewers for further evaluation.</p>
      <h2>Peer Review Process</h2>
      <p>GJMT follows a <strong>double-blind review process</strong>, where neither the authors nor the reviewers are aware of each other's identity. The review process generally takes <strong>2–4 months</strong>. Based on the reviewers' evaluation, a manuscript may be accepted, rejected, or accepted subject to revisions.</p>
      <h2>Publication Policy</h2>
      <p>GJMT is published <strong>biannually</strong>, with issues released in <strong>January</strong> and <strong>July</strong>.</p>
