      <p style="font-size:12px;color:#888;margin-top:6px;"><em>Note: Word limits exclude title, author details, abstract, keywords, tables, figures, and references.</em></p>

      <h2>3. Formatting Requirements</h2>
      <ul>
        <li>Font: Times New Roman throughout — headings, body, tables, references</li>
        <li>Body text: 12 pt, Regular, Justified, 1.5 line spacing</li>
        <li>Section headings: 14 pt Bold, ALL CAPS, numbered in Arabic decimal format</li>
        <li>Page size: A4 (210 × 297 mm); Margins: Top/Bottom 1 inch, Left 1.25 inch, Right 1 inch</li>
        <li>Abstract: 11 pt Italic, Justified, structured (Purpose / Methodology / Findings / Implications / Originality)</li>
        <li>Keywords: 5–8, alphabetically arranged, separated by semicolons</li>
      </ul>

      <h2>4. Structure of Main Text (IMRAD)</h2>
      <ul>
        <li><strong>1. Introduction</strong> — background, research gap, objectives, paper structure</li>
        <li><strong>2. Literature Review</strong> — theoretical framework; minimum 60% references from past 10 years</li>
        <li><strong>3. Research Methodology</strong> — design, sampling, instruments, analysis methods</li>
        <li><strong>4. Results and Analysis</strong> — findings with tables/figures cited before they appear</li>
        <li><strong>5. Discussion</strong> — interpretation, theoretical contributions, limitations</li>
        <li><strong>6. Conclusion</strong> — contributions, implications, policy recommendations, future research</li>
      </ul>

      <h2>5. Ethical Requirements</h2>
      <ul>
        <li>Similarity index must be <strong>below 15%</strong> (excluding references and quoted material)</li>
        <li>AI-generated content acceptable: <strong>0%</strong> — 100% original human-authored content required</li>
        <li>Ethics approval number must be stated in the methodology section (if applicable)</li>
        <li>Conflict of interest statement must be included before the references</li>
        <li>Funding acknowledgement must be included</li>
      </ul>

      <h2>6. Citation Style</h2>
      <p>GNA-JMT follows <strong>APA 7th Edition</strong> for all in-text citations and the reference list. All in-text citations must have a corresponding reference list entry and vice versa.</p>

      <h2>7. Submission Process</h2>
      <ul>
        <li>File format: Microsoft Word (.docx) only — PDF submissions not accepted</li>
        <li>Main manuscript must be <strong>fully anonymised</strong> for double-blind review</li>
        <li>Submit via email: <a href="mailto:gna.journal@gnauniversity.edu.in"><strong>gna.journal@gnauniversity.edu.in</strong></a></li>
        <li>Or via the submission portal: <a href="https://www.gnauniversity.edu.in/gna-journals" target="_blank">https://www.gnauniversity.edu.in/gna-journals</a></li>
      </ul>

